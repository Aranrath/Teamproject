package tp.model;

public class Statistic {
	
	private int id;
	private String title;
	
	public Statistic(String title)
	{
		this.title = title;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
	
	
	
	
	
	

}
